function dogSnapshot(dogIds: string[]) {
  return dogs
    .filter((dog) => dogIds.includes(dog.id))
    .map((dog) => ({
      dogId: dog.id,
      dogName: dog.name,
      feedingInstructions: dog.feedingInstructions,
      medicationInstructions: dog.medicationInstructions,
      behaviourNotes: dog.behaviourNotes,
      careNotes: dog.careNotes,
    }))
}

function quote(
  serviceId: string,
  startAt: string,
  endAt: string,
  dogCount: number,
) {
  const service =
    serviceTypes.find((item) => item.id === serviceId) ?? serviceTypes[0]
  return calculatePriceQuote({
    service,
    startAt,
    endAt,
    dogs: Array.from({ length: dogCount }, () => ({
      name: '',
      rateCents: service.defaultRateCents,
    })),
    hstRate: settings.hstRate,
    paymentMethod: 'etransfer',
  })
}

